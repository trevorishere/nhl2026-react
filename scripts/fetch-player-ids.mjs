/**
 * Fetches NHL player IDs for every player in BASE_DATA and writes
 * src/data/playerIds.js.  Re-run whenever the roster changes.
 *
 * Usage:  node scripts/fetch-player-ids.mjs
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));

// ── Load BASE_DATA ─────────────────────────────────────────────────────────────
const playersPath = path.join(__dirname, '../src/data/players.js');
const playersRaw  = fs.readFileSync(playersPath, 'utf8');
// Strip the ES module export so we can eval it in Node
const jsonStr = playersRaw.replace(/^export const BASE_DATA\s*=\s*/, '').replace(/;\s*$/, '');
const BASE_DATA = JSON.parse(jsonStr);

// ── NHL search API ─────────────────────────────────────────────────────────────
const SEARCH_URL = 'https://search.d3.nhle.com/api/v1/search/player';

async function findId(name, team) {
  const url = `${SEARCH_URL}?culture=en-us&limit=10&q=${encodeURIComponent(name)}&active=true`;
  try {
    const res  = await fetch(url);
    if (!res.ok) return null;
    const hits = await res.json();
    // Prefer exact name + team match, fall back to exact name only
    return (
      hits.find(p => p.name === name && p.teamAbbrev === team)?.playerId ??
      hits.find(p => p.name === name)?.playerId ??
      null
    );
  } catch {
    return null;
  }
}

// ── Main ───────────────────────────────────────────────────────────────────────
console.log(`Fetching IDs for ${BASE_DATA.length} players…`);
const out     = {};
let   matched = 0;
let   missed  = [];

for (let i = 0; i < BASE_DATA.length; i++) {
  const { name, team } = BASE_DATA[i];
  const id = await findId(name, team);

  if (id) {
    out[`${name}|${team}`] = id;
    matched++;
  } else {
    missed.push(`${name} (${team})`);
  }

  // Progress every 50 players
  if ((i + 1) % 50 === 0) {
    process.stdout.write(`  ${i + 1}/${BASE_DATA.length} — ${matched} matched\n`);
  }

  // Be polite to the API
  await new Promise(r => setTimeout(r, 120));
}

// ── Write output ───────────────────────────────────────────────────────────────
const outPath = path.join(__dirname, '../src/data/playerIds.js');
fs.writeFileSync(
  outPath,
  `// Auto-generated by scripts/fetch-player-ids.mjs\n// Re-run whenever the roster changes: node scripts/fetch-player-ids.mjs\nexport const PLAYER_IDS = ${JSON.stringify(out, null, 2)};\n`,
);

console.log(`\nDone: ${matched}/${BASE_DATA.length} matched`);
if (missed.length) {
  console.log(`Unmatched (${missed.length}):\n  ${missed.join('\n  ')}`);
}
console.log(`Written to src/data/playerIds.js`);
